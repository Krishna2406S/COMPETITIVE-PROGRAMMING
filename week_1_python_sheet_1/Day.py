Day_number = int(input("Enter the number from 1 to 7 : "))

if Day_number == 1:
    print("Monday")
elif Day_number == 2:
    print("Tuesday")
elif Day_number == 3:
    print("Wednesday")
elif Day_number == 4:
    print("Thursday")
elif Day_number == 5:
    print("Friday")
elif Day_number == 6:
    print("Saturday")
elif Day_number == 7:
    print("Sunday")
else:
    print("Invalid Input")


